Comparison ops always return boolean values (true or false bro)
Expressions or vals an op uses are its operands.
Expressions to left and right of operator are the operands

1.
  == 'is equal to' operator
2
  != - The "not equal to" operator. 
  asks if the left value is not equal to the right value
  
3. < -  "less than" symbol. 
asks if the left value is lower than the right value

4. > - The "greater than" symbol. 
asks if the left value is greater than the right value

5. <= - "less than or equal to" symbol. 
asks if the left value is less than or equal to the right value

6. >= - "greater than or equal to" symbol. 
asks if the value on the left is greater than or equal to the value on the right

