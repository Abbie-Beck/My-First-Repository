Puts v Return

the two can apparently be confusing to distinguish, like mod v remainder

  Puts: tells Ruby to print something to the screen
  It does not return what is printed to the screen
  
  Return: expressions return values in Ruby,
  returned values aren't nec. action that expr performs, but the result.
  
  Example: 
  
    irb :001 > puts 'stuff'
    stuff
    => nil
      ^stuff was printed, nil was returned
