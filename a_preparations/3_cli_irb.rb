CLI

to make directory (or folder) called new_dir type:
mkdir new_dir

to nav into this new folder type
cd new_dir

to create new file type
touch new_file.rb

to delete this file type
rm new_file

to nav out of curr folder to one level up type
cd ..

to remove the directory you created type
rmdir new_dir

to remove file and directory simultaneously type
rm -r new_dir



