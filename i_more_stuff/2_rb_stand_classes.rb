Ruby Standard Classes

Instead of trying to figure out which algorithms to use and 
trying to make your own operation, look for that action you want
to perform in Ruby's standard classes library. It will be 
more efficient. Don't reinvent the wheel.

You can calc the square root of a number by using Math.sqrt
You can get PI with constant Math::PI
It's cool that we don't know what constant syntax is 
Just know we are using Ruby's built-in libraries to solve our probs

(Note: it is Math.sqrt, not math.sqrt, because we are using class method, not 
an instance method like we have mostly used up to this point. Will discuss 
differences in depth in Obj Oriented Prog sections)

